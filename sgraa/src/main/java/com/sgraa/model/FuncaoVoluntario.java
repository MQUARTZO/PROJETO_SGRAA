package com.sgraa.model;

/**
 * Enumeração que representa as funções que um voluntário pode exercer no sistema.
 * Cada constante define um papel específico que um voluntário pode assumir.
 */
public enum FuncaoVoluntario {

    /**
     * Função de resgate de animais.
     * Voluntários com essa função são responsáveis por resgatar animais em situações de risco.
     */
    RESGATE,

    /**
     * Função de assistência aos animais.
     * Voluntários com essa função são responsáveis por cuidar, alimentar e fornecer suporte geral aos animais abrigados.
     */
    ASSISTENCIA
}
